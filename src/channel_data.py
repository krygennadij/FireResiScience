def get_channel_props_mm(profile_name):
    """
    Returns props in mm/mm2/mm4 for consistency with other modules.
    Input data is in cm.
    """
    if profile_name not in CHANNEL_DATA:
        return {}
    
    d = CHANNEL_DATA[profile_name]
    
    # Dimensions already in mm: h, b, s, t
    # Properties in cm -> convert to mm
    # A (cm2) -> mm2 (*100)
    # I (cm4) -> mm4 (*10000)
    # W (cm3) -> mm3 (*1000)
    # S (cm3) -> mm3 (*1000)
    # i (cm) -> mm (*10)
    
    return {
        "h": d["h"],
        "b": d["b"],
        "tw": d["s"],
        "tf": d["t"],
        "A": d["A"] * 100,
        "Ix": d["Ix"] * 10000,
        "Iy": d["Iy"] * 10000,
        "Wx": d["Wx"] * 1000,
        "Sx": d["Sx"] * 1000,
        "ix": d["ix"] * 10,
        "iy": d["iy"] * 10,
        "Wy": d["Wy"] * 1000, # Helper, likely unused but good to have
        "Af": d["b"] * d["t"], # Flange Area (approx b*t)
        "Aw": (d["h"] - 2 * d["t"]) * d["s"] # Web Area
    }
